/*****************************************************************************
 * FUNCTION
 *  srv_da_util_atoi
 * DESCRIPTION
 *  Convert string to S32. Note that if the value is larger that S32 can respresent,
 *  the value would be meanless.
 * PARAMETERS
 *  str     [IN]    String
 *  value   [OUT]
 * RETURNS
 *   If successfully parse str, returns MMI_TRUE.
 *****************************************************************************/
MMI_BOOL srv_da_util_atoi(const S8 *str, S32 *value)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    MMI_BOOL positive;
    register S32 v;
    register const S8 *p;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/

    positive = MMI_TRUE;
    p = str;

    if (*p == '-')
    {
        positive = MMI_FALSE;
        p++;
    }

    if (*p == '\0')
    {
        return MMI_FALSE;
    }

    v = 0;

    while (*p >= '0' && *p <= '9')
    {
        v = v * 10 + (S32)(*p - '0');
        p++;
    }

    if (!positive)
    {
        v = -v;
    }

    *value = v;

   if (*p == '\0')
	   return MMI_TRUE;
   else
	   return MMI_FALSE;
}


